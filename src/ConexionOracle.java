    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if((!jTextField1.getText().matches(""))){
            if((!jTextField2.getText().matches(""))){
                Map<String,String> opciones = BDLocal.getOpciones();
                opciones.put("urlconnection",jTextField1.getText());
                opciones.put("usuariodb",jTextField2.getText());
                opciones.put("clavedb",jTextField3.getText());
                if(BDLocal.saveOpciones(opciones)){
                    JOptionPane.showMessageDialog(null, "Se guard贸 exitosamente");
                }else{
                    JOptionPane.showMessageDialog(null, "No se guardaron las opciones");
                }
            }else{
                JOptionPane.showMessageDialog(null, "Debe ingresar un usuario");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe ingresar una cadena de conexi贸n");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

